#include<bits/stdc++.h>
using namespace std;
typedef long long int l;
int main()
{
    l t,n,ans=0;
    cin>>t>>n;
    char c[3];
    while(t--){
        l a;
        cin>>c>>a;
        if(c[0]=='+')
         n+=a;
        else if(c[0]=='-')
        {
         if(a>n)
          ans++;
         else
          n=n-a;
        }
    }
    cout<<n<<" "<<ans;
    return 0;
}
