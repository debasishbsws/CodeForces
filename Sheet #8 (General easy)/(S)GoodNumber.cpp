#include<bits/stdc++.h>
using namespace std;
int main()
{
    int k, n, i, j, m;
    int d[11];
    cin >>n>>k;
    int cnt = 0;
    for(i = 0; i < n; i++)
    {
        memset(d,0,sizeof(d));
        cin >>m;
        if(m == 0)
        {
            if(k == 0)
                cnt++;
            continue;
        }
        while(m)
        {
            d[m%10] = 1;
            m/= 10;
        }
        for(j=0;j<=k;j++)
        {
            if(!d[j])
                break;
        }
        if(j == k+1)
            cnt ++;
    }
    cout <<cnt<<endl;
    return 0;
}
