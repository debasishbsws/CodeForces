#include<bits/stdc++.h>
using namespace std;
typedef long long l;
l sum(l a,l b ,l  n)
{
 return (n)*(a+b)/2;
}
l even(l a,l b,l n){
     if(a%2==0&&b%2==0)
        return ((n+1)*(a+b))/4;
     if(a%2!=0&&b%2!=0)
       return (n-1)*((a+1)+(b-1))/4;
     if(a%2!=0&&b%2==0)
       return (n/2)*((a+1)+(b))/2;
     if(a%2==0&&b%2!=0)
       return (n/2)*((a)+(b-1))/2;             
}
l odd(l a,l b,l n){
    if(a%2==0&&b%2==0)
      return (n-1)*((a+1)+(b-1))/4;
    if(a%2!=0&&b%2!=0)
      return ((n+1)*(a+b))/4;
    if(a%2!=0&&b%2==0)
       return (n/2)*((a)+(b-1))/2;
    if(a%2==0&&b%2!=0)
       return (n/2)*((a+1)+(b))/2;   
}
 
int main(){
 l a,b;
 cin>>a>>b;
 l n=b-a+1;
 l m=a-b+1;
  if(a>b){
  cout<<sum(b,a,m)<<endl;
  cout<<even(b,a,m)<<endl;
  cout<<odd(b,a,m);} 
   else if(a<b){
  cout<<sum(a,b,n)<<endl;
  cout<<even(a,b,n)<<endl;
  cout<<odd(a,b,n);}
 
}
