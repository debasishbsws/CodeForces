#include<bits/stdc++.h>
using namespace std;
typedef long long int l; 
int main()
{
    int n, e;
     cin>>n;
    map <int, int> f;
    for(int i=1;i<=n;i++)
    {
        cin>>e;
        f[e]++;
    }
    int max_f = 0;
    for(map <int, int> :: iterator it = f.begin(); it != f.end(); it++)
        max_f= max(max_f, it->second);
    int half_size=n/2+n%2;
    cout<<(max_f>half_size?"NO\n":"YES\n");
    return 0;
}
