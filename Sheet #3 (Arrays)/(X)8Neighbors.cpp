#include<bits/stdc++.h>
using namespace std;
int main()
{
  int n,m;
   cin>>n>>m;
  char a[n][m];
   for(int r=0;r<n;r++)
    for(int c=0;c<m;c++)
      cin>>a[r][c];
  
  int x,y;
   cin>>x>>y;
  int c=0,p=0;
  for(int i=x-2;i<=x;i++)
   {for(int j=y-2;j<=y;j++)
    {
        if(i>=0&&i<n&&j>=0&&j<m)
          {
            if(i==x-1&&j==y-1)
               p;
            else{
              if(a[i][j]=='x')
                p++;
             c++;}
          }
    } 
    // cout<<endl;
   }
  if(c==p)
   cout<<"yes";
  else
   cout<<"no"; 
  
  return 0;
}
