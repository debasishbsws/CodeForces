#include<bits/stdc++.h>
using namespace std;
typedef long long int l; 
int main()
{
  int n;
  cin>>n;
  int a[n],c=0,p=0;
  for(int i=0;i<n;i++)
    cin>>a[i];
  sort(a,a+n);
  for(int i=0;i<n;i++)
  {
    if(a[i]==a[i+1])
     p++;
    else if(a[i]+1==a[i+1])
     { c++;
       c=c+p;
       p=0;
     }
      else if(a[i]+1!=a[i+1])
     {
       p=0;
     }
  }
  cout<<c;  
  return 0;
}
