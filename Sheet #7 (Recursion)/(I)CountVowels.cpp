int vowels(char s[], int low, int high)
{
    int sum=0;
    if (low!=high) {
        if ((s[low]=='a')||(s[low]=='A')||(s[low]=='e')|| 
            (s[low]=='E')||(s[low]=='i')||(s[low]=='I')|| 
            (s[low]=='o')||(s[low]=='O')||(s[low]=='u')|| 
            (s[low]=='U')) 
        {
            ++sum; 
        }
        sum+=vowels(s,low+1,high);
    }
    return sum;
}
